void main() {
  int x = 30;
  int y = 20;
  int z = 20;

  print("The example of Relational");
  bool res7 = z == y;//7.==
  print("z==y: $z===$y = $res7");

  bool res1 =  x > y; //1.>
  print("x>y: $x>$y = $res1");

  bool res2 = x<y;//2.<
  print("x<y: $x<$y = $res2");

  bool res3 = x >= y;//3.>=
  print("x>=y: $x<=$y = $res3");

  bool res4 = x <= y;//4.<=
  print("x<=y: $x<=$y = $res4");

  bool res5 = x != y;//5.!=
  print("x!=y: $x!=$y = $res5");

  bool res6 = x == y;//6.==
  print("x==y: $x==$y = $res6");

}
